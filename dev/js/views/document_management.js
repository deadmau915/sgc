/////////////////////////////// pagination /////////////////////////////////////

var cont_pag=-1;
var dates= 10;
var cont_dates= $("#document_management .content_table").length;
$("#document_management .content_table").hide();

// next pag
function next(){
    $("#document_management .content_table").hide();
    cont_pag= cont_pag+1;
    for(var i=0;i<dates;i++){
        $("#document_management .content_table:nth("+((cont_pag*dates)+i)+")").show();
    }
    console.log(cont_pag)
}

// prev pag
function prev(){
    $("#document_management .content_table").hide();
    cont_pag=cont_pag-1;
    for(var i=0;i<dates;i++){
        $("#document_management .content_table:nth("+((cont_pag*dates)+i)+")").show();
    }
    console.log(cont_pag)
}

next();

$("#next").click(function(){
    if((cont_dates/dates)-1 > cont_pag){
        next();
    }
});

$("#prev").click(function(){
    if(cont_pag>0){
        prev();
    }
});

